interface LanguageContextValue {
  language: Language
  setLanguage: (language: Language) => void
  t: (key: keyof typeof dictionaries.en, params?: TranslationParams) => string
  locale: typeof zhCN
  localeCode: string
}

const LanguageContext = createContext<LanguageContextValue>({
  language: 'zh',
  setLanguage: () => {},
  t: (key, params) => interpolate(dictionaries.zh[key], params),
  locale: zhCN,
  localeCode: 'zh-CN',
})

function interpolate(template: string, params?: TranslationParams) {
  if (!params) return template
  return Object.entries(params).reduce((result, [key, value]) => {
    return result.split(`{${key}}`).join(String(value))
  }, template)
}

export function LanguageProvider({
  children,
  language,
  setLanguage,
}: {
  children: ReactNode
  language: Language
  setLanguage: (language: Language) => void
}) {
  const dictionary = dictionaries[language]
  const value: LanguageContextValue = {
    language,
    setLanguage,
    t: (key, params) => interpolate(dictionary[key], params),
    locale: language === 'zh' ? zhCN : enUS,
    localeCode: language === 'zh' ? 'zh-CN' : 'en-US',
  }

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  return useContext(LanguageContext)
}
